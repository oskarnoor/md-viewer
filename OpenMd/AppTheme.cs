    public static IReadOnlyList<AppTheme> All => ThemeList;

    public static AppTheme FindByName(string? name)
    {
        return ThemeList.FirstOrDefault(theme => string.Equals(theme.Name, name, StringComparison.OrdinalIgnoreCase))
            ?? Light;
    }

    public static string ToHtmlColor(Color color) => ColorTranslator.ToHtml(color);

    private static Color Rgb(int value)
    {
        return Color.FromArgb((value >> 16) & 0xff, (value >> 8) & 0xff, value & 0xff);
    }
